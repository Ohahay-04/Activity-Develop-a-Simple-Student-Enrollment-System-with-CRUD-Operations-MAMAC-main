    private void btnUpdateCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateCourseActionPerformed
    try {
            
            int selectedRow = tblcourse.getSelectedRow();
            
            if (selectedRow == -1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please click on a course in the table to update!");
                return;
            }
            
            
          
            String originalName = tblcourse.getValueAt(selectedRow, 1).toString(); 
            
           
            String desc = txtDescription.getText();
            String creditsText = txtCredits.getText();
            
            if (desc.isEmpty() || creditsText.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please fill in both the Description and Credits to update.");
                return;
            }
            
            
            Course course = new Course();
            course.setCourseName(originalName); 
            course.setDescription(desc);
            course.setCredits(Integer.parseInt(creditsText));
            
            
            CourseDAO dao = new CourseDAO();
            dao.updateCourseByName(course);
      
            javax.swing.JOptionPane.showMessageDialog(null, "Course updated successfully!");
            
          
            loadTableData(); 
            txtDescription.setText("");
            txtCredits.setText("");
            txtCourseName.setSelectedIndex(-1);
            
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Credits must be a valid number.");
        } catch (Exception e) { 
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(null, "Update failed: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUpdateCourseActionPerformed

    private void btnDeleteCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteCourseActionPerformed
     
        int selectedRow = tblcourse.getSelectedRow();

        
        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please click on a course in the table to delete!");
            return;
        }

        
        
        String selectedName = tblcourse.getValueAt(selectedRow, 0).toString();
        String Name = tblcourse.getValueAt(selectedRow, 1).toString();

       
        int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
                "Are you sure you want to permanently delete the course: '" + Name + "'?", 
                "Confirm Delete", 
                javax.swing.JOptionPane.YES_NO_OPTION,
                javax.swing.JOptionPane.WARNING_MESSAGE);

        
        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try {
                
                CourseDAO dao = new CourseDAO();
                
                
                dao.deleteCourseByName(selectedName); 

                
                javax.swing.JOptionPane.showMessageDialog(this, "Course deleted successfully!");
                
                
                loadTableData(); 
                
                
                txtDescription.setText("");
                txtCredits.setText("");
                txtCourseName.setSelectedIndex(-1);
                
            } catch (Exception e) {
                e.printStackTrace();
                javax.swing.JOptionPane.showMessageDialog(this, "Error deleting course: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteCourseActionPerformed

    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
   String searchString = txtSearch.getText().trim();
        String selectedCategory = jComboBox1.getSelectedItem().toString(); 
        
        CourseDAO dao = new CourseDAO();
        List<Course> searchResults;

        if (searchString.isEmpty()) {
            searchResults = dao.getAllCourses(); 
        } else {
            searchResults = dao.searchCoursesByCategory(selectedCategory, searchString);
        }

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblcourse.getModel();
        model.setRowCount(0); 

        for (Course c : searchResults) {
            model.addRow(new Object[]{
                c.getCourseId(),
                c.getCourseName(),
                c.getDescription(),
                c.getCredits()
            });
        }
    }//GEN-LAST:event_txtSearchKeyReleased

