    private void loadTableData() {
   
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblcourse.getModel();
    
  
    model.setRowCount(0);
    
  
    CourseDAO dao = new CourseDAO();
    java.util.List<Course> list = dao.getAllCourses();
    
    
    for (Course course : list) {
        Object[] row = {
            course.getCourseId(),
            course.getCourseName(),
            course.getDescription(),
            course.getCredits()
        };
        model.addRow(row);
    }
}
    private void btnAddCourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddCourseActionPerformed
     
        java.awt.CardLayout card = (java.awt.CardLayout) jPanel2.getLayout();
        card.show(jPanel2, "card5");

        jPanel2.revalidate();
        jPanel2.repaint();
        
        int selectedIndex = txtCourseName.getSelectedIndex();
    
        
        if (selectedIndex == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a Course first!");
            return; 
        }

        try {
            
            if (txtDescription.getText().trim().isEmpty() || txtCredits.getText().trim().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Description and Credits cannot be empty!");
                return;
            }
            
            
            Course courseObj = new Course(); 
            courseObj.setCourseName(txtCourseName.getSelectedItem().toString());
            courseObj.setDescription(txtDescription.getText());
            courseObj.setCredits(Integer.parseInt(txtCredits.getText()));

         
            CourseDAO dao = new CourseDAO();
            dao.addCourse(courseObj); 
            
         
            javax.swing.JOptionPane.showMessageDialog(null, "Course Added Successfully!");
            
            
            loadTableData(); 
            
            
            txtDescription.setText("");
            txtCredits.setText("");
            
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Please enter a valid number for Credits.");
        } catch (Exception e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAddCourseActionPerformed

