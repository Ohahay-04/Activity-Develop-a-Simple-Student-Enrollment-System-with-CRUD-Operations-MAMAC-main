    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
int selectedRow = tblEnrollment.getSelectedRow();
        
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select an enrollment from the table to delete.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this enrollment?", "Warning", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            int enrollmentId = (int) tblEnrollment.getValueAt(selectedRow, 0);
            
            // Delegate to DAO
            boolean isDeleted = enrollmentDao.deleteEnrollment(enrollmentId);
            
            if (isDeleted) {
                JOptionPane.showMessageDialog(this, "Enrollment Deleted Successfully!");
                viewActionPerformed(null); // Refresh table
            } else {
                JOptionPane.showMessageDialog(this, "Enrollment ID not found in database.");
            }
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error deleting enrollment: " + e.getMessage()); 
        }
                                          
    }//GEN-LAST:event_deleteActionPerformed

