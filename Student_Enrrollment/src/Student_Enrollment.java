    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        String searchString = txtSearch.getText().trim();
        String selectedCategory = jComboBox1.getSelectedItem().toString(); 
        
        EnrollmentDAO dao = new EnrollmentDAO();
        List<Enrollment> searchResults;

        if (searchString.isEmpty()) {
            searchResults = dao.getAllEnrollments(); 
        } else {
            searchResults = dao.searchEnrollmentsByCategory(selectedCategory, searchString);
        }

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblEnrollment.getModel();
        model.setRowCount(0); 

        for (Enrollment e : searchResults) {
            model.addRow(new Object[]{
                e.getEnrollment_Id(),
                e.getStudent_Id(), 
                e.getCourse_Id(),  
                e.getEnrollment_Date()
            });
        }
    }//GEN-LAST:event_txtSearchKeyReleased

    private void viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewActionPerformed
        try {
            DefaultTableModel model = (DefaultTableModel) tblEnrollment.getModel();
            model.setRowCount(0); // Clear existing table data

            model.setColumnIdentifiers(new String[]{"Enrollment ID", "Student", "Course", "Date"});

            // Delegate to DAO
            List<Enrollment> enrollments = enrollmentDao.getAllEnrollments();

            for (Enrollment e : enrollments) {
                Object[] row = {
                    e.getEnrollment_Id(),
                    e.getStudent_Id(), // MATCHED
                    e.getCourse_Id(),  // MATCHED
                    e.getEnrollment_Date()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading table data: " + e.getMessage());
        }
    }//GEN-LAST:event_viewActionPerformed

