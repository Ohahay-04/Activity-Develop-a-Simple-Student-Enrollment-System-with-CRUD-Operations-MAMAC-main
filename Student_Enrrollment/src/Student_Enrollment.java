    private void jComboBox1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jComboBox1KeyReleased
       // 1. Get the text typed in the search bar
        String searchString = txtSearch.getText().trim();
        
        // 2. Get the option currently selected in the ComboBox
        String selectedCategory = jComboBox1.getSelectedItem().toString(); 
        
        // 3. Get your table's model (NOTE: Change 'tblcourse' to your actual table name if you are on a different screen!)
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblEnrollment.getModel(); 
        javax.swing.table.TableRowSorter<javax.swing.table.DefaultTableModel> sorter = new javax.swing.table.TableRowSorter<>(model);
        tblEnrollment.setRowSorter(sorter); 

        // 4. Apply the filter based on the ComboBox selection
        if (searchString.isEmpty()) {
            sorter.setRowFilter(null); // If search is empty, show everything
        } else {
            // THE FIX: We tell the RowFilter exactly which column index to look at!
            // Column indexes start at 0 (0 = 1st column, 1 = 2nd column, etc.)
            
            switch (selectedCategory) {
                case "Course ID":
                    // Only searches Column 0
                    sorter.setRowFilter(javax.swing.RowFilter.regexFilter("(?i)" + searchString, 0)); 
                    break;
                case "Course Name":
                    // Only searches Column 1
                    sorter.setRowFilter(javax.swing.RowFilter.regexFilter("(?i)" + searchString, 1)); 
                    break;
                case "Description":
                    // Only searches Column 2
                    sorter.setRowFilter(javax.swing.RowFilter.regexFilter("(?i)" + searchString, 2)); 
                    break;
                case "Credits":
                    // Only searches Column 3
                    sorter.setRowFilter(javax.swing.RowFilter.regexFilter("(?i)" + searchString, 3)); 
                    break;
                default:
                    // If they select "All" (or anything else), search everywhere!
                    sorter.setRowFilter(javax.swing.RowFilter.regexFilter("(?i)" + searchString)); 
                    break;
            }
        }     // TODO add your handling code here:
    }//GEN-LAST:event_jComboBox1KeyReleased

