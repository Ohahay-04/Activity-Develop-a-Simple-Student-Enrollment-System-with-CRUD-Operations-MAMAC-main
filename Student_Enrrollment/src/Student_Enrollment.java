    private void populateComboBoxes() {
   try {
            cmbStudentName.removeAllItems();
            cmbCourseName.removeAllItems();

            // Fetch students and add ID to the front!
            List<Student> students = studentDao.getAllStudents();
            for (Student s : students) {
                // IMPORTANT: This forces the dropdown to show "1 - Rey Christian Magbanua"
                String fullStudentName = s.getStudentId() + " - " + s.getFirstName() + " " + s.getLastName();
                cmbStudentName.addItem(fullStudentName);
            }

            // Fetch courses and add ID to the front!
            List<Course> courses = courseDao.getAllCourses();
            for (Course c : courses) {
                // IMPORTANT: This forces the dropdown to show "101 - Programming"
                String fullCourseName = c.getCourseId() + " - " + c.getCourseName();
                cmbCourseName.addItem(fullCourseName);
            }

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading combo box data: " + e.getMessage());
        }
}
    private void enrollActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enrollActionPerformed
if(cmbStudentName.getSelectedItem() == null || cmbCourseName.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Please select a student and a course.");
            return;
        }

        try {
            Enrollment newEnrollment = new Enrollment();
            
            // 1. Get the full text from the combo boxes
            String selectedStudent = cmbStudentName.getSelectedItem().toString();
            String selectedCourse = cmbCourseName.getSelectedItem().toString();
            
            // 2. Split the text at the " - " and grab ONLY the first part (the ID number)
            // .trim() removes any accidental spaces like "1 " so it perfectly becomes "1"
        String studentIdOnly = selectedStudent.split("-")[0].trim();
        String courseIdOnly = selectedCourse.split("-")[0].trim();
            
            // 3. Save only the IDs to the database!
            newEnrollment.setStudent_Id(studentIdOnly);
            newEnrollment.setCourse_Id(courseIdOnly);
            
            newEnrollment.setEnrollment_Date(LocalDate.now().toString());

            // Delegate to DAO
            enrollmentDao.addEnrollment(newEnrollment);
            
            JOptionPane.showMessageDialog(this, "Student Enrolled Successfully!");
            viewActionPerformed(null); // Refresh table
            
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error enrolling student: " + e.getMessage());
        }
    }//GEN-LAST:event_enrollActionPerformed
   
