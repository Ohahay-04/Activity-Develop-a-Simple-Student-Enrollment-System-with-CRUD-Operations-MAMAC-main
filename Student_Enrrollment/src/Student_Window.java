     private void loadTableData() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblStudents.getModel();
        String[] columnNames = {"Student ID", "First Name", "Last Name", "Age", "Email Address"};
        model.setColumnIdentifiers(columnNames); // ma change ang name sa table
        model.setRowCount(0); // Clear current table

        java.util.List<Student> list = new StudentDAO().getAllStudents();
        for (Student s : list) {
            model.addRow(new Object[]{
                s.getStudentId(),
                s.getFirstName(),
                s.getLastName(),
                s.getAge(),
                s.getEmail()
            });
        }
    }
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (txtFirstName.getText().isEmpty() || txtLastName.getText().isEmpty() || txtEmail.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "All fields are required!");
            return;
        }

        // 2. Check if Age is a valid number
        try {
            int age = Integer.parseInt(txtAge.getText());
            if (age <= 0 || age > 120) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid age (1-120).");
                return;
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Age must be a number!");
            return;
        }

        try {
            Student student = new Student();
            student.setFirstName(txtFirstName.getText());
            student.setLastName(txtLastName.getText());
            student.setAge(Integer.parseInt(txtAge.getText()));
            student.setEmail(txtEmail.getText());

            StudentDAO dao = new StudentDAO();
            dao.addStudent(student);

            javax.swing.JOptionPane.showMessageDialog(this, "Student Added!");
            loadTableData(); // Refresh the list
            txtFirstName.setText("");
            txtLastName.setText("");
            txtAge.setText("");
            txtEmail.setText("");
        } catch (HeadlessException | NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }   // TODO add your handling code here:
                                           
    }//GEN-LAST:event_btnAddActionPerformed

