    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = tblStudents.getSelectedRow();

        if (selectedRow == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a student from the table to update.");
            return;
        }

        try {
            // 1. Get the ID from the first column of the selected row
            int id = (int) tblStudents.getValueAt(selectedRow, 0);

            // 2. Create a Student object with the NEW data from the TextFields
            Student student = new Student();
            student.setStudentId(id);
            student.setFirstName(txtFirstName.getText());
            student.setLastName(txtLastName.getText());
            student.setAge(Integer.parseInt(txtAge.getText()));
            student.setEmail(txtEmail.getText());

            // 3. Call the DAO to update the database
            new StudentDAO().updateStudent(student);

            // 4. Feedback and Refresh
            javax.swing.JOptionPane.showMessageDialog(this, "Student Updated Successfully!");
            loadTableData(); // Refresh the table to show changes
            clearFields(); 

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        } // TODO add your handling code here:
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblStudents.getSelectedRow();
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a student from the table.");
        return;
    }
    
    int id = (int) tblStudents.getValueAt(selectedRow, 0);
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, "Delete this record?", "Confirm", javax.swing.JOptionPane.YES_NO_OPTION);
    
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        new StudentDAO().deleteStudent(id);
        loadTableData();
    }   // TODO add your handling code here:  // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

