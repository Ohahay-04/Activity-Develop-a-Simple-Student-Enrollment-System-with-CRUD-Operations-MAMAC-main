    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
                                     
String searchString = txtSearch.getText().trim();
        String selectedCategory = jComboBox1.getSelectedItem().toString(); 
        
        StudentDAO dao = new StudentDAO();
        List<Student> searchResults;

        if (searchString.isEmpty()) {
            searchResults = dao.getAllStudents(); 
        } else {
            searchResults = dao.searchStudentsByCategory(selectedCategory, searchString);
        }

        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblStudents.getModel();
        model.setRowCount(0); 

        for (Student s : searchResults) {
            model.addRow(new Object[]{
                s.getStudentId(),
                s.getFirstName(),
                s.getLastName(),
                s.getAge(),
                s.getEmail()
            });
        }
    }//GEN-LAST:event_txtSearchKeyReleased

